package OOP_Inheritance;

public class Car {
	
	public void start() {
		System.out.println("Car---started");
	}
	public void stop() {
		System.out.println("Car---stop");
	}
	public void fuel() {
		System.out.println("Car---fuel");
	}
	
	public static void count() {
		System.out.println("car--------count");
	}

}
